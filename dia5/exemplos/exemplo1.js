
// Exemplo Prático 1
// Percorrendo uma lista de clientes

// Cria um array chamado clientes contendo quatro nomes
let clientes = ["Aline", "Júlia", "Carlos", "Renata"];

// Inicia um loop que percorre o array do índice 0 até o último índice
for (let i = 0; i < clientes.length; i++) {
    // Exibe no console uma mensagem personalizada para cada cliente
    console.log(`Enviando mensagem para ${clientes[i]}...`);
}

// Saída:
// Enviando mensagem para Aline...
// Enviando mensagem para Júlia...
// Enviando mensagem para Carlos...
// Enviando mensagem para Renata...
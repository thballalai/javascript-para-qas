// Crie um loop for que exiba no console todos os múltiplos de 3 entre 3 e 30.

// Criação de estrutura de repetição do tipo para
for(let i = 3; i < 30; i++){
    // verifica se o número da iteração tem resto 0 ao ser dividido por 3 para exibir uma mensagem na tela
    if(i%3 == 0){
        console.log(`O número ${i} é multiplo de 3.`)
    }
}


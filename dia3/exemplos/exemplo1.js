// Exemplo Prático 1
// Verificando Aumento Salarial

// Declaramos uma variável para armazenar o sexo
let sexo = "feminino";
// Declaramos uma variável par armazenar os anos na empresa
let anosNaEmpresa = 12;

// Avaliamos se o sexo for feminino E o número de anos na empresa
// For maior ou igual a 10, imprime "Vai ganhar aumento de 10%"
// Se não, imprime "Não vai ganhar aumento." 
if(sexo == "feminino" && anosNaEmpresa >= 10){
    console.log("Vai ganhar aumento de 10%.");
} else {
    console.log("Não vai ganhar aumento.");
}

// Saída: Vai ganhar aumento de 10%
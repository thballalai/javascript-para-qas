// Exemplo Prático 2
// Juntar textos de forma dinâmica

// Declaramos uma variável primeiroNome para armazenar o primeiro nome de uma pessoa 
let primeiroNome = "Lucas";
// Declaramos uma variável sobrenome para armazenar o sobrenome de uma pessoa
let sobrenome = "Silva";
// Usamos o + para concatenar (juntar) várias strings (textos)
console.log("Bem vindo, " + primeiroNome + " " + sobrenome + "!");

// Saída: Bem vindo, Lucas Silva!
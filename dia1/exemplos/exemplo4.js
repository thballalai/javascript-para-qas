//Exemplo Prático 1
//Acessar informações de um objeto com dados de um carro

//Declaramos a variável carro, com os atributos marca, modelo e ano e seus respectivos valores
let carro = {
    marca: "Porsche",
    modelo: "718 Caynan GT4",
    ano: 2025
};
// usamos o formato variavel.atributo (ex.: carro.marca) para acessar um dado em um objeto
console.log("Carro: " + carro.marca + " " + carro.modelo);

//Saída: Carro: Porsche 718 Caynan GT4
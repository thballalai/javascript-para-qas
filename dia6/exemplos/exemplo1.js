// Exemplo Prático 1
// Adicionando e Removendo Elementos:

// Declara um array chamado tarefas com três elementos
let tarefas = ["Estudar", "Treinar", "Ler"];

// Adiciona o item "Caminhar" ao final do array
tarefas.push("Caminhar");

// Remove o primeiro elemento do array (no caso, "Estudar")
tarefas.shift();

// Exibe no console o array utilizado
console.log(tarefas);

// Saída ["Treinar", "Ler", "Caminhar"]
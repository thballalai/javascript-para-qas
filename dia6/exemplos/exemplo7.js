// Exemplo Prático 7
// Removendo o último elemento com pop()

// Declara um array chamado "cidades" com quatro nomes de cidades
let cidades = ["Braga", "Porto", "Coimbra", "Lisboa"];

// Remove o último elemento do array (no caso, "Lisboa")
cidades.pop();

// Exibe no console o array atualizado
console.log(cidades);

// Saída: ["Braga", "Porto", "Coimbra"]

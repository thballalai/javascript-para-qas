// Exemplo Prático 2
// Percorrendo um Array com um Loop

// Declara um array chamado "meusNumeros" com quatro números
let meusNumeros = [5, 10, 15, 20];

// Inicia um loop que percorre o array do primeiro ao último elemento
for (let i = 0; i < meusNumeros.length; i++){
    console.log(`Número ${i+1}: ${meusNumeros[i]}`);
}
// Exemplo Prática 6
// Transformando um Array em uma string

// Declara um array chamado ingredientes com três itens
let ingredientes = ["Farinha", "Ovos", "Leite"];

// Usa o metódo join para transformar o array em uma string separada por " + "
let receita = ingredientes.join(" + ");

// Exibe no console a string formatada
console.log(`Ingredientes: ${receita}`);

// Saída: Farinha + Ovos + Leite
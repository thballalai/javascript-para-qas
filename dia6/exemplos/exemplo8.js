// Exemplo Prático 8
// Adicionando um elemento no início com unshift()

// Declara um array chamado filaDeAtendimento com três clientes
let filaDeAtendimento = ["Carlos", "Fernanda", "João"];

// Adiciona "Ana" no início da fila
filaDeAtendimento.unshift("Ana");

// Exibe no console o array atualizado
console.log(filaDeAtendimento);

// Saída: ["Ana", "Carlos", "Fernanda", "João"]
// Localizando um Elemento no Array

// Declara um array chamado times com três nomes de clubes de futebol
let times = ["Porto", "Real Madrid", "Manchester City"];

// Encontra a posição (índice) do time "Real Madrid" dentro do array
let posicao = times.indexOf("Real Madrid");

// Exibe no console a posição do time dentro do array
console.log(`O time está na posição: ${posicao}`);